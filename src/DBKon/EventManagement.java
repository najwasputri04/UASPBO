    // ===================== LOAD DATA =====================
    private void loadEventData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        String sql = "SELECT event_id, event_name, location, event_date, description, status FROM event";
        try (java.sql.Connection con = new Koneksi().con;
             java.sql.Statement st = con.createStatement();
             java.sql.ResultSet rs = st.executeQuery(sql)) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("event_id"),
                    rs.getString("event_name"),
                    rs.getString("location"),
                    rs.getString("event_date"),
                    rs.getString("description"),
                    rs.getString("status")
                });
            }
        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
    }

    // ===================== UPDATE QUICK INFO =====================
    private void updateQuickInfo() {
        String sql = "SELECT COUNT(*) FROM event";
        try (java.sql.Connection con = new Koneksi().con;
             java.sql.Statement st = con.createStatement();
             java.sql.ResultSet rs = st.executeQuery(sql)) {
            if (rs.next()) {
                jLabel21.setText("Total Events: " + rs.getInt(1));
            }
        } catch (java.sql.SQLException e) {
            jLabel21.setText("Total Events: -");
        }
    }

    // ===================== SEARCH =====================
    private void searchEvent(String keyword) {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        String sql = "SELECT event_id, event_name, location, event_date, description, status FROM event "
                   + "WHERE event_name LIKE ? OR location LIKE ? OR status LIKE ?";
        try (java.sql.Connection con = new Koneksi().con;
             java.sql.PreparedStatement ps = con.prepareStatement(sql)) {
            String kw = "%" + keyword + "%";
            ps.setString(1, kw);
            ps.setString(2, kw);
            ps.setString(3, kw);
            java.sql.ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("event_id"),
                    rs.getString("event_name"),
                    rs.getString("location"),
                    rs.getString("event_date"),
                    rs.getString("description"),
                    rs.getString("status")
                });
            }
        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal search: " + e.getMessage());
        }
    }

