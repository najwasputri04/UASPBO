// ===================== EDIT =====================
private void editEvent() {
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit!");
        return;
    }
    int id          = (int) jTable1.getValueAt(selectedRow, 0);
    String name     = (String) jTable1.getValueAt(selectedRow, 1);
    String location = (String) jTable1.getValueAt(selectedRow, 2);
    String date     = String.valueOf(jTable1.getValueAt(selectedRow, 3));
    String desc     = (String) jTable1.getValueAt(selectedRow, 4);
    String status   = (String) jTable1.getValueAt(selectedRow, 5);

    updateEvent form = new updateEvent(id, name, location, date, desc, status);
    form.setVisible(true);
    form.addWindowListener(new java.awt.event.WindowAdapter() {
        public void windowClosed(java.awt.event.WindowEvent e) {
            loadEventData();
            updateQuickInfo();
        }
    });
}

    // ===================== DELETE =====================
    private void deleteEvent() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus!");
            return;
        }
        int eventId      = (int) jTable1.getValueAt(selectedRow, 0);
        String eventName = (String) jTable1.getValueAt(selectedRow, 1);

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
            "Yakin hapus event \"" + eventName + "\"?",
            "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm != javax.swing.JOptionPane.YES_OPTION) return;

        String sql = "DELETE FROM event WHERE event_id=?";
        try (java.sql.Connection con = new Koneksi().con;
             java.sql.PreparedStatement ps = con.prepareStatement(sql)) {
            ps.setInt(1, eventId);
            ps.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Event berhasil dihapus!");
            loadEventData();
            updateQuickInfo();
        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal hapus: " + e.getMessage());
        }
    }
    
    
